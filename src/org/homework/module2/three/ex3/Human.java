package org.homework.module2.three.ex3;

public final class Human {
    private final int age;
    private final int height;

    public Human(int age, int height) {
        this.age = age;
        this.height = height;
    }

    public int getAge() {
        return age;
    }

    public int getHeight() {
        return height;
    }
}
